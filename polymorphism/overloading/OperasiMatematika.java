public class OperasiMatematika {
    // Overloading 1: penjumlahan dua bilangan
    int tambah(int a, int b) {
        return a + b;
    }

    // Overloading 2: penjumlahan tiga bilangan
    int tambah(int a, int b, int c) {
        return a + b + c;
    }

    // Overloading 3: penjumlahan bilangan desimal
    double tambah(double a, double b) {
        return a + b;
    }
}
